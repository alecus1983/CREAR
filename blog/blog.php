	    <h2 class="mb-4 mt-4">Disciplina positiva y porqué se portan mal los niños</h2>

	    <P>los niños son niños y a veces pueden llegar a desesperarnos cuando no cumplen nuestras expectativas. Tal vez allí yace el primer error: "esperar algo de ellos". En primer lugar, no debemos esperar que su comportamiento se ajuste a ciertos estándares. Debemos comprender que su forma de comunicarse difiere de la de los adultos y que detrás de su conducta hay un mensaje que necesitamos descifrar para llegar a la raíz del problema.

		<br><br>

		Rudolf Dreikurs mencionó: "Cuando los niños se comportan mal, están intentando comunicar algo de manera diferente a la de un adulto, y por lo tanto, percibimos su comportamiento como incorrecto, sin detenernos a pensar que quizás es la única forma en la que pueden expresarse". Los motivos pueden ser variados: buscan nuestra atención, están aburridos, tienen hambre, o están cansados. Aquí es donde entra en juego la empatía con nuestros hijos para comprender el motivo detrás de sus comportamientos.</P>



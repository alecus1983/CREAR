            <p style="font-we" class="fw-normal">Todos los papás deseamos lo mejor para nuestros hijos, pero a veces no tenemos el conocimiento adecuado para actuar correctamente. Educar no es sencillo. Por eso, es crucial contar con recursos que nos guíen desde el entendimiento y la amabilidad. La crianza positiva es esa herramienta que nos proporciona técnicas para educar a los niños desde la colaboración y el respeto mutuo entre padres e hijos.</p>

            <h2 class="mb-4 mt-4">¿Qué es la disciplina positiva?</h2>

	    <P>La crianza positiva fue desarrollada por el psicoterapeuta Alfred Adler y su discípulo, el psiquiatra y educador Rudolf Dreikurs. Posteriormente, Jane Nelsen y Lynn Lott (fundadoras de la Asociación Americana de Crianza Positiva) sistematizaron sus principios y técnicas para su aplicación. A menudo, al escuchar la palabra "crianza", la asociamos con connotaciones negativas, pero su verdadero significado es "criar". Podemos considerar a nuestros hijos como aprendices que requieren nuestra orientación durante su desarrollo, con el fin de convertirse en individuos libres, felices e independientes en el futuro.
	    </P>


	    <h2 class="mb-4 mt-4">Firm & Kind</h2>

	    <P>Son varios los estudios que han demostrado que los niños tienen la necesidad de crear vínculos con las personas más cercanas a ellos; como sus padres, amigos o profesores. Los niños que mantienen esos vínculos fuertes, son niños que por lo general, se comportan mejor. La disciplina positiva se basa en la premisa “Firm & Kind” (firme pero amable) y se encarga de reforzar esos vínculos mediante el respeto, la compresión, autoestima y la comunicación.</P>


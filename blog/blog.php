                <h2 class="mb-4 mt-4">Disciplina positiva no es igual a ser permisivo</h2>

		<P>Disciplina positiva no significa ausencia de esta y por lo tanto exceso de permisividad. Tampoco se parece a la disciplina punitiva (basada en el castigo), ya que esta hace que los niños aprendan  a base de sentirse mal y des del sentimiento de miedo. Con la disciplina positiva, lo que buscamos son resultados a largo plazo. El objetivo, es que los niños entiendan porque están mal ciertas cosas y porque no deben hacerlas. No imponemos castigos sin saber si los niños han comprendido realmente el porque no deben actuar de ciertas maneras.</P>




